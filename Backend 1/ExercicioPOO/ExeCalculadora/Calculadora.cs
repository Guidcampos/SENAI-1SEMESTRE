namespace ExeCalculadora
{
    public class Calculadora
    {
        
        public float n1;
        public float n2;



        public float Somar(){
          return n1 + n2;
     
          
        
        }
        public float Subtrair(){
         return n1 - n2;
          
        }
        public float Dividir(){
          return n1 / n2;

        }
        public float Multiplicar(){
           return n1 * n2;
          
        }


    }
}